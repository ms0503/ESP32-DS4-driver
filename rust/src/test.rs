use super::*;

#[test]
fn test_get_checksum() {
    let test_data1: [u8; LENGTH] = [0x80, 0x47, 0x8f, 0x2f, 0x10, 0xde, 0x22, 0x8a, 0x22, 0x00];
    let test_data2: [u8; LENGTH] = [0x80, 0x7a, 0x54, 0xc2, 0x63, 0xee, 0x4b, 0xed, 0x3d, 0x00];
    let test_data3: [u8; LENGTH] = [0x80, 0x83, 0x72, 0x64, 0x4f, 0xd9, 0x26, 0x02, 0x9c, 0x00];
    assert_eq!(Packet::get_checksum(&test_data1), 0x41);
    assert_eq!(Packet::get_checksum(&test_data2), 0xd6);
    assert_eq!(Packet::get_checksum(&test_data3), 0xc5);
}
